package Game;

public class Personnage {
	
	/**
	 * LES VARIABLES
	 */
	
	int x,y;

	public Personnage(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
	
}
